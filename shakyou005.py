# メソッド
print("hello".upper())  # <- 小文字を大文字に
print("Hello".replace("o", "@"))  # <- 文字の置換

# リスト
fruit = ["Apple", "Orange", "Pear"]
print(fruit)
# リストに要素を追加
fruit.append("Banana")
fruit.append("Peach")
print(fruit)

random = []  # <- 空のリスト
random.append(True)
random.append(100)
random.append(1.1)
random.append("Hello")
print(random)
